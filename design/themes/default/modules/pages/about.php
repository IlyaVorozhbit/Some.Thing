            <li>Существенное увеличение посещаемости сервера - засчет популярности проекта и засчет грамотной политики в отношении "молодых" серверов</li><br>
            <li>Возможность собрать игроков вашего сервера в его аудиторию - мы и правда делаем все возможное для этого</li>
        </ul>


    <p>Оперативный отдел <a title="В скором времени будет открыт набор в техподдержку">техподдержки</a> быстро решит все возникшие при работе с сервисом проблемы и предотвратит их дальнейшее появление.</p>

    <p>Одним из главных достоинств сайта является <b>минимальная вероятность накрутки голосов</b>, а в связи с этим - на первых местах рейтинга действительно достойные сервера. </p>

